    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int num_row = table.getSelectedRow();
        if( num_row < 0){
            return ;
        }
        else{
            
            try {
            // TODO add your handling code here:
            Dao.Connect();
            Connection connection = Dao.getConnection();
            
            String query = "DELETE FROM \"utilisateur\" WHERE login = ?";
            PreparedStatement pst = connection.prepareStatement(query);
            pst.setString(1, table.getValueAt(num_row, 0).toString());
            int result = pst.executeUpdate();
        } catch (SQLException ex) {
            Logger.getLogger(FormUtilisateur.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        remplirTable();
        
    }//GEN-LAST:event_jButton3ActionPerformed

