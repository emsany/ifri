    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
          int num_row = table.getSelectedRow();
        if( num_row < 0){
            return ;
        }
        else{
            
            Utilisateur utilisateur = new Utilisateur();
            utilisateur.setLogin(table.getValueAt(num_row, 0).toString());
            utilisateur.setNom(table.getValueAt(num_row, 1).toString());
            utilisateur.setPrenom(table.getValueAt(num_row, 2).toString());
            utilisateur.setMotpasse(table.getValueAt(num_row, 3).toString());
            FormUtilisateur form = new FormUtilisateur("modification", utilisateur);
            form.setVisible(true);
        }
        remplirTable();
    }//GEN-LAST:event_jButton4ActionPerformed

