    public  void remplirTable(){
         DefaultTableModel model = (DefaultTableModel) table.getModel();
        while( model.getRowCount() > 0){
            model.removeRow(0);
        }
        try {
            // TODO add your handling code here:
            Dao.Connect();
            Connection connection = Dao.getConnection();
            String query = "SELECT * FROM \"COURS\"";
            Statement st = connection.createStatement();
            ResultSet rs = st.executeQuery(query);
            while( rs.next()){
                model.addRow(new String[]{ rs.getString("code_classe"),rs.getString("code_enseignant"), rs.getString("code_matiere"),rs.getString("objectif"),rs.getString("libelle_classe"),rs.getString("nom_enseignant"),rs.getString("libelle_matiere")});
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormCours.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

